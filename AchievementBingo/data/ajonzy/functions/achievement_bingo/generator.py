import random

generated_achievements = []
index_choices = list(range(len(achievements)))
random.shuffle(index_choices)

while len(generated_achievements) < 25:
    random_index = index_choices.pop()
    random_achievement = achievements[random_index]
    generated_achievements.append(random_achievement)

file_builder = open("game/track_achievements.mcfunction", "w+")

for index, achievement in enumerate(generated_achievements):
    row = (index // 5) + 1
    col = (index % 5) + 1
    file_builder.write(f'execute as @a[advancements={{{achievement["id"]}=true}},scores={{Row{row}Col{col}=0}}] run scoreboard players add @s Row{row}Col{col} 1\n')

file_builder.close()